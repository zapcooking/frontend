  <h1 class="text-3xl font-bold mb-8">Why Zap Cooking Exists</h1>

  <div class="flex flex-col gap-6 text-gray-700 leading-relaxed">
    <p>Food has always been social.</p>

    <p>Long before feeds, likes, or algorithms, recipes were shared person to person. Around tables. In kitchens. Through stories, notes, and small variations passed along over time. Food was never about optimization or scale. It was about connection.</p>

    <p>Zap Cooking exists to bring that spirit into the modern internet.</p>

    <p>Most food content today lives inside platforms designed to extract attention. Algorithms decide what you see. Ads shape what gets promoted. Creators produce value but rarely own their audience or benefit directly from their work.</p>

    <p class="font-medium">Zap Cooking takes a different approach.</p>

    <p>This is a Nostr native space where recipes are shared as open events, identity belongs to the individual, and value flows peer to peer. Discovery comes from people, not ranking systems. Creativity compounds through conversation, remixing, and shared curiosity.</p>

    <p>The goal is not to be the biggest recipe app.</p>

    <p>The goal is to create a place where food culture can live openly and grow naturally.</p>

    <h2 class="text-2xl font-bold mt-8 mb-4">A Note From the Founder</h2>

    <p>Zap Cooking started with a simple curiosity. What would food sharing look like if it lived on open rails?</p>

    <p>I have spent much of my life building and working inside systems where small improvements compound and trust matters. When I discovered Nostr and Lightning, it felt like a natural extension of that thinking. Identity that travels. Value that is voluntary. Participation without permission.</p>

    <p>Food felt like the right place to apply it.</p>

    <p>Zap Cooking is not a finished product. It is something in motion. It evolves in public and improves through use. While this page reflects where Zap Cooking began, it does not belong to me alone. It is already shaped by the people who share recipes, offer feedback, contribute code, and show up with curiosity.</p>

    <p>I help prepare the ground and tend the space. What grows here is up to the community.</p>

    <h2 class="text-2xl font-bold mt-8 mb-4">What This Place Is About</h2>

    <p>Zap Cooking is built on a few simple beliefs:</p>

    <ul class="flex flex-col gap-2 pl-0 list-none">
      <li>Food connects people</li>
      <li>Culture matters more than clicks</li>
      <li>Creators should be supported directly</li>
      <li>Identity should travel with you</li>
      <li>Communities grow stronger when participation is easy</li>
    </ul>

    <p>If you believe those things too, you already belong here.</p>

