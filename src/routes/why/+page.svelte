    <h2 class="text-2xl font-bold mt-8 mb-4">Why Nostr</h2>

    <p>Zap Cooking is built on Nostr because it lets people participate without giving anything up.</p>

    <p>You do not create an account.</p>
    <p>You do not hand over your data.</p>
    <p>You do not get tracked, ranked, or pushed by algorithms.</p>

    <p>Your identity belongs to you.</p>
    <p>Your recipes belong to you.</p>
    <p>Your relationships move with you.</p>

    <p>Nostr makes it possible for Zap Cooking to be open without being chaotic, social without being extractive, and simple without being controlled.</p>

    <p>It also means Zap Cooking is not a closed platform. It is one place in a broader network of people and tools that can grow and change over time.</p>

    <p>If you have never used Nostr before, that is okay. You can explore, read, and learn at your own pace. There is no pressure to understand the technology to enjoy the culture.</p>

    <p>Under the hood, Nostr keeps things honest. On the surface, it simply feels more human.</p>

    <h2 class="text-2xl font-bold mt-8 mb-4">Open by Design</h2>

    <p>Zap Cooking is built in the open.</p>

    <p>The code is public. The ideas are shared. The project evolves through use, feedback, and contribution.</p>

    <p>You do not need to be a developer to participate. Sharing recipes, testing features, and offering thoughtful feedback are just as important as writing code.</p>

