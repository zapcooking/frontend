  <h1 class="text-3xl font-bold mb-8" style="color: var(--color-text-primary)">Privacy Policy</h1>

  <div class="flex flex-col gap-6 leading-relaxed" style="color: var(--color-text-primary)">
    <p class="text-sm opacity-75">Last updated: December 26, 2025</p>

    <p>Zap Cooking ("Zap Cooking," "we," "our," or "us") respects your privacy and is committed to transparency. This Privacy Policy explains how information is handled when you use our website, mobile applications, and related services (collectively, the "Service").</p>

    <p>Zap Cooking is built on decentralized technologies and is designed to minimize the collection and retention of personal data.</p>

    <h2 class="text-2xl font-bold mt-8 mb-4" style="color: var(--color-text-primary)">1. Information We Collect</h2>

    <h3 class="text-xl font-semibold mt-6 mb-3" style="color: var(--color-text-primary)">Information you provide</h3>

    <p>Zap Cooking does not currently require or collect email addresses, real names, phone numbers, or traditional account credentials.</p>

    <p>Users interact with the Service using decentralized identifiers and cryptographic keys provided by the Nostr protocol. Any content you choose to publish, such as recipes, comments, images, or profile information, is user-generated and controlled by you.</p>

    <p>Zap Cooking does not collect or retain this content on centralized servers operated by us. Content is transmitted directly between the app and decentralized relays selected by the user.</p>

    <p>If optional features are introduced in the future (such as email-based support or notifications), this Privacy Policy will be updated before those features are enabled.</p>

    <h3 class="text-xl font-semibold mt-6 mb-3" style="color: var(--color-text-primary)">Technical information</h3>

    <p>The Service may process limited, non-identifying technical information locally on a user's device for reliability and performance purposes, such as application state or error handling. This information is not used to identify users and is not retained on centralized servers.</p>

    <h2 class="text-2xl font-bold mt-8 mb-4" style="color: var(--color-text-primary)">2. How Information Is Used</h2>

    <p>Information is handled only to:</p>

    <ul class="list-disc pl-6 space-y-2">
      <li>Provide and operate the Zap Cooking Service</li>
      <li>Enable community interaction and content sharing</li>
      <li>Maintain performance, reliability, and security</li>
      <li>Comply with applicable legal obligations</li>
    </ul>

    <p>Zap Cooking does not sell personal data and does not use data for targeted advertising or profiling.</p>

    <h2 class="text-2xl font-bold mt-8 mb-4" style="color: var(--color-text-primary)">3. Decentralized Data and Public Content</h2>

    <p>Zap Cooking leverages the Nostr protocol, which means:</p>

    <ul class="list-disc pl-6 space-y-2">
      <li>Content you choose to publish may be publicly accessible on decentralized relays</li>
      <li>Zap Cooking does not control third-party relays or their data retention practices</li>
      <li>Public content may persist independently of Zap Cooking</li>
    </ul>

    <p>You are responsible for managing your cryptographic keys and for the content you choose to make public.</p>

    <h2 class="text-2xl font-bold mt-8 mb-4" style="color: var(--color-text-primary)">4. Local Storage</h2>

    <p>Zap Cooking may use local storage on your device to maintain session state or user preferences. We do not use tracking or advertising cookies.</p>

    <h2 class="text-2xl font-bold mt-8 mb-4" style="color: var(--color-text-primary)">5. Sharing of Information</h2>

    <p>Zap Cooking does not share personal information with third parties.</p>

    <p>Limited service providers may be used to operate the application itself (such as website or application hosting). These providers do not receive user content and are subject to confidentiality obligations.</p>

    <h2 class="text-2xl font-bold mt-8 mb-4" style="color: var(--color-text-primary)">6. Data Security</h2>

    <p>We take reasonable technical and organizational measures to protect the Service. However, decentralized systems rely on user-controlled credentials, and security ultimately depends on how users manage their keys.</p>

    <p>No method of transmission or storage is 100% secure.</p>

    <h2 class="text-2xl font-bold mt-8 mb-4" style="color: var(--color-text-primary)">7. Data Retention</h2>

    <p>Zap Cooking does not retain user content or personal data on centralized servers operated by us.</p>

    <p>Public content published through decentralized protocols may persist on third-party relays, even if you stop using the Service.</p>

    <h2 class="text-2xl font-bold mt-8 mb-4" style="color: var(--color-text-primary)">8. Children's Privacy</h2>

    <p>Zap Cooking is intended for users aged 18 and older and is not designed for children under the age of 13. We do not knowingly collect personal information from children.</p>

    <h2 class="text-2xl font-bold mt-8 mb-4" style="color: var(--color-text-primary)">9. Changes to This Policy</h2>

    <p>We may update this Privacy Policy from time to time. Any changes will be posted on this page with an updated effective date.</p>

